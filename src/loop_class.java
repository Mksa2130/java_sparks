public class loop_class {
    public static void main(String[] args)
    {
        for(int i=1; i<=5; i++)
        {
            System.out.println(i);
        }
        //while
        int a=0;
        while(a<4)
        {
            System.out.println("While loop "+a);
            a++;
        }
    }
}
